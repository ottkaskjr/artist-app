////////////////////////////////////////////////////
////// fix broken links (remove whitespaces, ///////
///////////// line breaks and tabs) ////////////////
////////////////////////////////////////////////////
let divs = document.querySelectorAll('.navbar-menu-item');

for (let div of divs) {
  let a = div.firstChild;
  let href = a.getAttribute('href')
  href = href.replace(/\s/g,'')
  a.setAttribute('href', href)
}

